public class OperasiPerbandingan_10 {
    public static void main(String[] args) {
        // Operator Perbandingan
        // > : lebih dari
        // < : kurang dari
        // >= : lebih dari sama dengan
        // <= : kurang dari sama dengan
        // == : sama dengan
        // != : tidak sama dengan
        // hasil boolean

        int a = 100;
        int b = 10;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);

    }
}
