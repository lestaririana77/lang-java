public class UnaryOperator_09a {
    public static void main(String[] args) {

        // Unary operator
        // ++ = a + 1
        // -- = a - 1
        // - = negatif
        // + = positif
        // ! = Boolean kebalikan

        int d = +100;
        int e = -10;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);

        System.out.println(!true);
    }
}
