public class Tipe_Data_Character_03 {
    public static void main(String[] args) {

        char r = 'R';
        char i = 'I';
        char a = 'A';
        char n = 'N';

        System.out.print(r);
        System.out.print(i);
        System.out.print(a);
        System.out.print(n);
        System.out.print(a);
        // kalau print aja, outputnya gak ada enternya
        System.out.println();
        System.out.println(r);
        System.out.println(i);
        System.out.println(a);
        System.out.println(n);
        System.out.println(a);
    }
}
