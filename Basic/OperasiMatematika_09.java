public class OperasiMatematika_09 {
    public static void main(String[] args) {

        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        // Augmented Assigments
        // a = a+10 -> a += 10
        // a = a-10 -> a -= 10
        // a = a*10 -> a *= 10
        // a = a/10 -> a /= 10
        // a = a%10 -> a %= 10

        int c = 100;

        c += 10;
        System.out.println(c);

        c -= 10;
        System.out.println(c);

        c *= 10;
        System.out.println(c);
    }
}
