public class Tipe_Data_Number_01 {

    public static void main(String[] args) {

        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 1000000;
        long iniLong = 1000000000;
        long iniLong2 = 1000000000L;

        // Floating number
        float iniFloat = 10.12F;
        double iniDouble = 12.2424;

        // Literals
        int decimalInt = 25;
        int hexInt = 0xA132B;
        int binInt = 0b01010101;

        // Underscore = sebagai pemisah
        long balance = 1_000_000_000_000L;
        int sum = 60_000_000;

    }
}
