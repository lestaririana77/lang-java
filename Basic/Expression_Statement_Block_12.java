public class Expression_Statement_Block_12 {
    public static void main(String[] args) {

        // EXPRESSION = komponen dari statement, operator/variable yg menghasilkan
        // single value
        int value;
        value = 10;

        // STATEMENT = kalimat lengkap dalam bahasa pemrograman
        // sebuah execution komplit, biasanya diakhiri titik koma

        // assigment statement
        double aValue = 8933.234;
        // incrument statement
        aValue++;
        // method invocation statement
        System.out.println(aValue);
        // object creation statement
        // Date date = new Date();

        // BLOCK = kumpulan dari nol atau lebih statement
        // diawali dan diakhiri kurung kurawal {}

        {
            System.out.println("Hello World");
        }

    }
}
